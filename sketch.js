function drawArrows(arrowSpeed, arrowHitMargin) {
  timeSinceStart = Date.now() - songStartTime

  noteSpeed = 0.6;

  fill(255)

  // arrows for left arrow

  // imageMode(CENTER)
  for (let noteType = 0; noteType < notes.length; noteType++) {
    for (let i = 0; i < notes[noteType].length; i++) {
      if (notes[noteType][i] - timeSinceStart < -500) {
        notes[noteType].splice(0, 1)
        miss();
      }
    }

    arrowImage = leftArrow;
    noteWidth = noteSize;
    noteHeight = noteSize;
    if (noteType == 0) {
      arrowImage = leftArrow;
      noteWidth = noteSize;
      noteHeight = noteSize * 1.8;
    } else if (noteType == 1) {
      arrowImage = downArrow;
      noteWidth = noteSize * 1.8;
      noteHeight = noteSize;
    } else if (noteType == 2) {
      arrowImage = upArrow;
      noteWidth = noteSize * 1.8;
      noteHeight = noteSize;
    } else if (noteType == 3) {
      arrowImage = rightArrow;
      noteWidth = noteSize;
      noteHeight = noteSize * 1.8;
    }

    for (let i = 0; i < notes[noteType].length; i++) {
      if (notes[noteType][i] - timeSinceStart > 5000) break;
      image(arrowImage, (startPoint+noteSpacing*noteType)+random(-2*noteShake, 2*noteShake)+50 * sin((timeSinceStart - notes[noteType][i]) * 0.01), (timeSinceStart - notes[noteType][i]) * noteSpeed + random(-2*noteShake, 2*noteShake) + height - 90, noteWidth, noteHeight)

      print((timeSinceStart - notes[noteType][i]) * noteSpeed + random(-2*noteShake, 2*noteShake))
    }
  }
}

